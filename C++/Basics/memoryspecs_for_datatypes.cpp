#include <bits/stdc++.h>
using namespace std;

int main() {
    int a; //declaration
    a=12; //initialization

    cout<<"size of int "<<sizeof(a)<<endl;

    float b;
    cout<<"size of float "<<sizeof(b)<<endl;

    char c;
    cout<<"size of character "<<sizeof(c)<<endl;

    bool d;
    cout<<"size of boolean is "<<sizeof(d)<<endl;

    long int l;
    short int s;

    cout<<"size of long int is "<<sizeof(l)<<endl;
    cout<<"size of short int is "<<sizeof(s)<<endl;

}